# strings : ordered, immutable, text representation

mystring = "Hello World" # you can use either single or double quotes
print(mystring)

# Be careful using single quotes inside the single quotes, use \ to escape your single quote
# otherwise use single quotes inside the double quotes

mystr1 = 'I\'m a programmer'
# or
mystr2 = "I'm a programmer"

print(mystr1)
print(mystr2)

# Triple quotes is used for multiline strings and this is also used for documentation inside the code

mystr3 = """ Hello
world"""
print(mystr3)

# using \ inside triple quotes : to continue in same line or one line
mystr4 = """ Hello \
world"""
print(mystr4)


