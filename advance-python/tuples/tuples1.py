# Tuple : ordered, immutable, allows duplicate elements

mytuple = ('Max', 28, 'Boston')
print(mytuple)

mytuple1 = 'Max', 28, 'Boston' # parantheses is optional
print(type(mytuple1))

mytuple2 =('Max')   # this is not tuple
print(type(mytuple2))  # this is string type,  the syntax is ('Max',)

mytuple3 =('Max',)
print(type(mytuple3))


