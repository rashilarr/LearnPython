# bool data type

x = True                # bool type is used for logical operation and expression
print(f'x is {x}')
print(type(x))
                        # bool values are True and False
y = False 
print(f'y is {y}')
print(type(y))

z = 7 > 5              # output shows result of operation i.e true
print(f'z is {z}')
print(type(z))


a = None               # None type indicates the absence of value 
print(f'a is {a}')
print(type(a))

if a:                  # None value is used to perform the else block
    print("True")
else:
    print("False")


b = 0               # int type 
print(f'b is {b}')
